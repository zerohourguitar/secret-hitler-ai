package com.secrethitler.ai.dtos;

public class GameplayAction {
	private String action;
	private String[] args;
	
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public String[] getArgs() {
		return args;
	}
	public void setArgs(String[] args) {
		this.args = args;
	}
}
